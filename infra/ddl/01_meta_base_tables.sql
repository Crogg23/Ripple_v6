-- Meta base tables (NOT rebuildable from raw — these hold the run-log + registry STATE).
-- Idempotent: CREATE ... IF NOT EXISTS never clobbers live data. Re-create structure only;
-- the ROWS must be restored from a separate export (see README).

-- LIBRARY_META.INGEST_LOGS.INGEST_RUNS
CREATE TABLE IF NOT EXISTS LIBRARY_META.INGEST_LOGS.INGEST_RUNS (
	SOURCE_ID VARCHAR(16777216) NOT NULL,
	RUN_ID VARCHAR(16777216) NOT NULL,
	STARTED_AT TIMESTAMP_NTZ(9),
	ENDED_AT TIMESTAMP_NTZ(9),
	STATUS VARCHAR(16777216),
	ROW_COUNT NUMBER(38,0),
	FILE_BYTES NUMBER(38,0),
	SHA256 VARCHAR(64),
	SOURCE_URL VARCHAR(16777216),
	MESSAGE VARCHAR(16777216),
	_LOADED_AT TIMESTAMP_NTZ(9) DEFAULT CAST(CONVERT_TIMEZONE('UTC', CAST(CURRENT_TIMESTAMP() AS TIMESTAMP_TZ(9))) AS TIMESTAMP_NTZ(9))
);

